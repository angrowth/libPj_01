#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dst;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(s1);
	if (!(dst = (char *)malloc((size + 1) * sizeof(char))))
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
