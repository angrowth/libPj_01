#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	size_t	res;
	int	sign;

	i = 0;
	res = 0;
	sign = 1;
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		sign *= -1;
		i++;
	}
	if (str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (res >= 9223372036854775808UL && sign == -1)
		return 0;
	if (res >= 9223372036854775807UL && sign == 1)
		return -1;
	return (sign * res);
}
