#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	if (!(ptr = (void *)malloc(size * count)))
		return (NULL);
	ft_memset(ptr, 0, size*count);
	return (ptr);
}
